import { assert, assertEquals } from "../test_deps.ts";
import isValid from "./isValid.ts";

Deno.test("isValid returns expected", () => {
  assert(isValid("18591404-6"));
  assert(isValid("24225112-1"));
  assert(isValid("10980661-7"));
  assert(isValid("17116316-1"));
  assert(isValid("13463387-5"));
  assert(isValid("23455756-4"));
  assert(isValid("16961522-5"));
  assert(isValid("20141432-6"));
  assert(isValid("19497051-k"));
  assert(isValid("23870961-k"));
  assert(isValid("185914046"));
  assert(isValid("242251121"));
  assert(isValid("109806617"));
  assert(isValid("171163161"));
  assert(isValid("134633875"));
  assert(isValid("234557564"));
  assert(isValid("169615225"));
  assert(isValid("201414326"));
  assert(isValid("19497051k"));
  assert(isValid("23870961k"));
  assert(isValid(185914046));
  assert(isValid(242251121));
  assert(isValid(109806617));
  assert(isValid(171163161));
  assert(isValid(134633875));
  assert(isValid(234557564));
  assert(isValid(169615225));
  assert(isValid(201414326));
  assert(isValid("18.591.404-6"));
  assert(isValid("24.225112-1"));
  assert(isValid("10.980.661-7"));
  assert(!isValid(Infinity));
  assert(!isValid("18591404-5"));
  assert(!isValid("24225112-K"));
  assert(!isValid("10980661-0"));
  assert(!isValid("17116316-2"));
  assert(!isValid("13463387-K"));
  assert(!isValid("23455756-8"));
  assert(!isValid("16961522-3"));
  assert(!isValid("20141432-9"));
  assert(!isValid("19497051-3"));
  assert(!isValid("23870961-1"));
  assert(!isValid("dummyvalue"));
});
